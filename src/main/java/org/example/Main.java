package org.example;

public class Main {
//Lớp này chứa phương thức main(), là phương thức chính của chương trình.
    public static void main(String[] args) {
        System.out.println("Hello world!");
    }
//Phương thức này là điểm bắt đầu của chương trình.
//Trong phương thức này, có một lệnh in ra chuỗi "Hello world!" bằng cách sử dụng phương thức println() của lớp System.
}
//chương trình của bạn là một ứng dụng Java đơn giản với phương thức main() để in ra màn hình chuỗi "Hello world!".